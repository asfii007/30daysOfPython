# Escape Sequences in Python
print("1. Single Quote (\\'):")
print('   It\'s a sunny day!')

print("2. Backslash (\\\\):")
print("   This is a backslash: \\")

print("3. New Line (\\n):")
print("   Hello\n   World")

print("4. Carriage Return (\\r):")
print("   Hello\rWorld")
print("   (World overwrites Hello)")


print("5. Tab (\\t):")
print("   Name:\tPython")


print("6. Backspace (\\b):")
print("   Helloo\b!")
print("   (The extra 'o' is removed)")


print("7. Form Feed (\\f):")
print("   Hello\fWorld")


print("8. Octal Value (\\ooo):")
print("   \110\145\154\154\157")   # "Hello"


print("9. Hex Value (\\xhh):")
print("   \x48\x65\x6C\x6C\x6F")   # "Hello"

